function splitify(str) {
  return str.split(/\W+/);
}

console.log(splitify("Hello World,I-am code"));

//This implementation uses the split() method with a regular expression (/\W+/) as the argument. 
//The regular expression \W+ matches one or more non-word characters (such as punctuation or whitespace), effectively splitting the string wherever a non-word character is found. 
//The resulting array is then returned by the function.
